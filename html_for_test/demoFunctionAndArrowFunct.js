//Demo arrow function and normal function
// Date: 02/01/2021

//Normal function
function normalFunction() {
    console.log("I am normal function");
}

//Arrow function
let arrowFunction = () => {
    console.log("I am arrow function");
}

//Calling normal function

normalFunction();

//Calling arrow function

arrowFunction();

//Output
//I am normal function
//I am arrow function

//Normal function

//Normal function is a function that is defined using the function keyword. It is also known as a traditional function.

//Arrow function

//Arrow function is a new way of defining functions in JavaScript. It is a more concise way of writing functions. Arrow functions do not have their own this value, so they are not suitable for defining methods. Arrow functions are best suited for non-method functions.

